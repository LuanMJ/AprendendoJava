
public class TiposNumericos {
	
	public static void main(String[] args) {
		
		Byte numeroByte = 127; //-128 até 127
		
		Integer numeroInteger = -32758; // -32758 até 32757
		
		Long numeroLong = 2147483647L; // -2147483648 até 2147483647
		
		Float numeroFloat = 1.0f;
		Double numeroDouble = 1.0;
	}

}
